// Variables - containers of values
// const, var, let

// let - editable/mutable variable
let firstName = "Jared";
console.log("Firstname:", firstName);

firstName = "Ariston";
console.log("Firstname:", firstName);

// const - uneditable/immutable variable
const lastName = "Hortaleza";
console.log("Lastname:", lastName);

