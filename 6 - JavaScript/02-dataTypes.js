// Primitive Data Types - singular value

// Number: Represents numeric values (100, -12, 3.14)
let age = 25;
console.log("Age:", age);
console.log("Typeof age:", typeof age);

// Strings: Represents textual values ("Anything in", 'quotes')
let firstName = "Nancy";
console.log("Firstname:", firstName);
console.log("Typeof firstname:", typeof firstName);

// Boolean: Either true or false
let isAdmin = true;
console.log("Isadmin:", isAdmin);
console.log("Typeof isadmin:", typeof isAdmin);

// Null: INTENTIONAL absence of value
let car = null;
console.log("Car?", car);
console.log("Typeof car:", typeof car);

// Undefined: UNINTENTIONAL absence of value
let city;
console.log("City:", city);
console.log("Typeof city:", typeof city);